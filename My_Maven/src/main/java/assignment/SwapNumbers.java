package assignment;

public class SwapNumbers {

	public static void main(String[] args) {
		
		System.out.println("Before Swap");
		System.out.println("------------");
		int a=10;
		int b=20;
		int c;
		System.out.println("a="+a);
		System.out.println("b="+b);
		
		System.out.println("After Swap");
		System.out.println("------------");
		c=a;
		a=b;
		b=c;
		System.out.println("a="+a);
		System.out.println("b="+b);

	}

}
