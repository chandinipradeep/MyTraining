package java_self_training;

public class Class1 {
	
	public int a=10;

	private int b=20;
	
	protected int c=30;
	
	int d=35;
	
	public static void main(String[] args) {
		
		Class1 ob=new Class1();
		System.out.println(ob.a);
		System.out.println(ob.b);
		System.out.println(ob.c);
		System.out.println(ob.d);

	}

}
